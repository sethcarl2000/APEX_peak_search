bool contains_nan(const std::vector<double>& v)
{
    for (const auto& x : v) if (is_nan(x)) { return true; }
    return false;  
}
//____________________________________________________________________________________________________________________
double n_choose_k(int n, int k)
{
#ifdef DEBUG
    std::printf("%i choose %i: ", n,k);
#endif
    if (k > n) return numbers::nan; 
    if (n < 1 || n == k) return 1.; 
    
    double ret = ((double)n)/((double)n-k);
    while (--n > k) { 
        ret *= ((double)n)/((double)n-k);
    }
#ifdef DEBUG
    std::printf("%f\n", ret);
#endif
    return ret; 
}
//____________________________________________________________________________________________________________________
double log_factorial(int n) 
{
    double sum=0.; while (n>1) { sum += std::log(n--); } 
    return sum; 
}
